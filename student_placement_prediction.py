import streamlit as st
import pandas as pd
import joblib

# Load model and encoder

model = joblib.load("Student_placement_prediction_model.pkl")
encoder = joblib.load("label_encoder_placement.pkl")

st.title("Student Placement Prediction")
st.write("Enter student details")

# Inputs

branch = st.selectbox("Branch", encoder["branch"].classes_)
college_tier = st.selectbox("College Tier", encoder["college_tier"].classes_)

cgpa = st.number_input("CGPA", 0.0, 10.0, 7.0)
backlogs = st.number_input("Backlogs", 0, 10, 0)
coding_skills = st.number_input("Coding Skills", 0, 10, 5)
dsa_score = st.number_input("DSA Score", 0, 100, 50)
aptitude_score = st.number_input("Aptitude Score", 0, 100, 50)
communication_skills = st.number_input("Communication Skills", 0, 10, 5)
ml_knowledge = st.number_input("ML Knowledge", 0, 10, 3)
system_design = st.number_input("System Design", 0, 10, 3)

internships = st.number_input("Internships", 0, 10, 1)
projects_count = st.number_input("Projects", 0, 10, 2)
certifications = st.number_input("Certifications", 0, 10, 1)
hackathons = st.number_input("Hackathons", 0, 10, 0)
open_source_contributions = st.number_input("Open Source Contributions", 0, 10, 0)
extracurriculars = st.number_input("Extracurricular Activities", 0, 10, 1)

if st.button("Predict Placement"):
    
  df = pd.DataFrame({
    "branch":[branch],
    "college_tier":[college_tier],
    "cgpa":[cgpa],
    "backlogs":[backlogs],
    "coding_skills":[coding_skills],
    "dsa_score":[dsa_score],
    "aptitude_score":[aptitude_score],
    "communication_skills":[communication_skills],
    "ml_knowledge":[ml_knowledge],
    "system_design":[system_design],
    "internships":[internships],
    "projects_count":[projects_count],
    "certifications":[certifications],
    "hackathons":[hackathons],
    "open_source_contributions":[open_source_contributions],
    "extracurriculars":[extracurriculars]
})

# Encode categorical columns
for col in ["branch","college_tier"]:
    df[col] = encoder[col].transform(df[col])


prediction = model.predict(df)

if prediction[0] == 1:
    st.success("Student is likely to be Placed")
else:
    st.error("Student is NOT likely to be Placed")

